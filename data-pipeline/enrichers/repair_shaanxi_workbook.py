def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("source", type=Path)
    parser.add_argument("--in-place", action="store_true")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    src = args.source
    if not src.exists():
        raise FileNotFoundError(src)

    out = src if args.in_place else src.with_name(f"{src.stem}_repaired{src.suffix}")
    wb = load_workbook(src)
    ws = wb[wb.sheetnames[0]]

    headers = [ws.cell(1, c).value for c in range(1, ws.max_column + 1)]
    pos = {name: idx + 1 for idx, name in enumerate(headers)}

    for row_idx, fields in CORRECTIONS.items():
        for field_name, value in fields.items():
            ws.cell(row_idx, pos[field_name]).value = value

    serial_col = pos["序号"]
    for row_idx in range(2, ws.max_row + 1):
        ws.cell(row_idx, serial_col).value = row_idx - 1

    wb.save(out)
    print(f"OUTPUT: {out}")
    print(f"ROWS: {ws.max_row - 1}")


if __name__ == "__main__":
    main()
